package com.rolecall;

import android.view.LayoutInflater;
import android.view.View;
import android.view.ViewGroup;
import android.widget.TextView;

import androidx.annotation.NonNull;
import androidx.recyclerview.widget.RecyclerView;

import com.example.rolecall.R;

import java.util.ArrayList;

public class ChatroomMessageAdapter
        extends RecyclerView.Adapter<ChatroomMessageAdapter.ViewHolder>{

    // Code sourced from example at https://developer.android.com/develop/ui/views/layout/recyclerview#java

    private ArrayList<String> chatMessages;

    /**
     * Provide a reference to the type of views that you are using
     * (custom ViewHolder)
     */
    public static class ViewHolder extends RecyclerView.ViewHolder {
        private final TextView textView;

        public ViewHolder(View view) {
            super(view);
            // Define click listener for the ViewHolder's View
            textView = (TextView) view.findViewById(R.id.textView);
        }

        public TextView getTextView() {
            return textView;
        }
    }

    /**
     * Initialize the dataset of the Adapter
     *
     * @param dataSet String[] containing the data to populate views to be used
     * by RecyclerView
     */
    public ChatroomMessageAdapter(ArrayList<String> dataSet) {
        chatMessages = dataSet;
    }

    // Create new views (invoked by the layout manager)
    @NonNull
    @Override
    public ViewHolder onCreateViewHolder(ViewGroup viewGroup, int viewType) {
        // Create a new view, which defines the UI of the list item
        View view = LayoutInflater.from(viewGroup.getContext())
                .inflate(R.layout.text_row_item, viewGroup, false);

        return new ViewHolder(view);
    }

    public void sendMessage(String newMessage) {
        chatMessages.add(newMessage);
        this.notifyItemInserted(chatMessages.size()-1);
    }

    // Replace the contents of a view (invoked by the layout manager)
    @Override
    public void onBindViewHolder(ViewHolder viewHolder, final int position) {

        // Get element from your dataset at this position and replace the
        // contents of the view with that element
        viewHolder.getTextView().setText(chatMessages.get(position));
    }

    @Override
    public int getItemCount() {
        return chatMessages.size();
    }
}
